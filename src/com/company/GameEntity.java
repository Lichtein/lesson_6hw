package com.company;

public class GameEntity {
       private int health;
       private int damage;
       private String superAbility;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getSuperAblity() {
        return superAbility;
    }

    public void setSuperAblity(String superAblity) {
        this.superAbility = superAblity;
    }
}
